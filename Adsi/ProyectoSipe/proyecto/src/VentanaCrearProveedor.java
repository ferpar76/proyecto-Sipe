    public void ingresarProveedor() {
        try {

            Class.forName("com.mysql.jdbc.Driver");
            Connection miConexion = DriverManager.getConnection("jdbc:mysql://localhost:3306/sipe", "root", "");
            System.out.println("conexion establecidad");
            PreparedStatement miSentencia = miConexion.prepareStatement("CALL insertar_proveedor(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");

            miSentencia.setString(1, cajaCodigo.getText());
            miSentencia.setString(2, cajaNombre.getText());
            miSentencia.setString(3, cajaRazon.getText());
            miSentencia.setString(4, cajaNit.getText());
            miSentencia.setString(5, cajaDireccion.getText());
            miSentencia.setString(6, comboCiudad.getSelectedItem().toString());
            miSentencia.setString(7, cajaPostal.getText());
            miSentencia.setString(8, comboPais1.getSelectedItem().toString());
            miSentencia.setString(9, cajaEmail.getText());
            miSentencia.setString(10, cajaFax.getText());
            miSentencia.setString(11, cajaTel.getText());
            miSentencia.setString(12, cajaContactoVende.getText());
            miSentencia.setString(13, cajaUrl.getText());
            miSentencia.setString(14, comboDiaEntrega.getSelectedItem().toString());
            miSentencia.setString(15, cajonNotas.getSelectedText());
            miSentencia.executeUpdate();

        } catch (ClassNotFoundException ce) {
            ce.printStackTrace();
        } catch (SQLException se) {
            se.printStackTrace();
        }
    }

